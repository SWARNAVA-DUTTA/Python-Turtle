t.forward(200)

#Blue Circle
t.speed(15)
t.goto(56.24,198.32)
t.pendown()
t.color('navy')
t.begin_fill()
t.circle(40)
t.end_fill()
t.penup()

#White Circle
t.color('white')
t.goto(56.24,194.5)
t.pendown()
t.begin_fill()
t.circle(36)
t.end_fill()
t.penup()

#Blue Mini Dots
t.goto(60.24,125.32)
t.pendown()
t.color('navy')
for i in range(24):
    t.begin_fill()
    t.circle(2)
    t.end_fill()
    t.penup()
    t.forward(9)
    t.right(15.5)
    t.pendown()
t.penup()
t.goto(56.5,160.11)

#Blue Spikes
t.pensize(2)
t.pendown()
for i in range(23):
    t.forward(36)
    t.backward(36)
    t.left(15.5)
t.left(15.5)

#Green
t.speed(3)
t.pensize(1)
