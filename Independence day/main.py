import turtle
wn = turtle.Screen()
wn.bgcolor("white")
wn.title("Happy Independence Day")
t = turtle.Turtle()
t.speed(3)
t.penup()
t.goto(-150,-200)
t.pendown()

#Stick
t.left(90)
t.color('brown')
t.begin_fill()
t.forward(500)
t.right(90)
t.forward(10)
t.right(90)
t.forward(500)
t.left(90)
t.end_fill()

# 1st Base
t.color('black')
t.begin_fill()
t.forward(50)
t.right(90)
t.forward(10)
t.right(90)
t.forward(100)
t.right(90)
t.forward(10)
t.right(90)
t.forward(100)
t.end_fill()
t.right(90)
t.forward(10)
t.penup()
t.forward(3)
t.left(90)
t.pendown()

#2nd Base
t.begin_fill()
t.forward(50)
t.right(90)
t.forward(10)
